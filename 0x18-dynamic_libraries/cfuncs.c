#include "cfuncs.h"

/**
 * add - adds two numbers
 * @a: a
 * @b: b
 * Return: a + b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts b from a
 * @a: a
 * @b: b
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: a
 * @b: b
 * Return: a * b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides a by b
 * @a: a
 * @b: b
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - calculates a mod b
 * @a: a
 * @b: b
 * Return a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
